
public class Ejercicio01 {

// Ejercicio 1: Escribir un programa donde se muestren los 10 primeros números enteros

/*
1. Voy a usar un bucle for porque sé cuántas veces quiero repetir la acción.
2. El bucle empezará en 1 y terminará en 10.
3. En cada vuelta del bucle, mostraré el número por pantalla usando System.out.println.
*/


    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

    }
}