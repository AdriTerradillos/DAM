package Primera_Evaluacion.Practica;

public class operadoresAritmeticos02 {

    public static void main(String[] args) {

        /**
         * === Operadores Aritméticos ===
         --> Suma: ++
         --> Resta: --
         --> Multiplicación: *
         --> División: /
         --> Módulo: %
         */


         // Ejercicio 1 (suma y resta)

         int numero1 = 12;
         int numero2 = 7;

         int suma = numero1 + numero2;
         int resta = numero1 - numero2;

         System.out.println("EL resultado de la suma es: " + suma + ".");
         System.out.println("El resultado de la resta es: " + resta + ".");


System.out.println("");


         // Ejercicio 2 (multiplicación y división)

         int numero3 = 48;
         int numero4 = 2;

         int multiplicacion = numero3 * numero4;
         int division = numero3 / numero4;

         System.out.println("El resultado de la multiplicación es: " + multiplicacion + ".");
         System.out.println("El reultado de la división es: " + division + ".");


System.out.println("");


         // Ejercicio 3 (módulo o %)

         int numero5 = 40;
         int numero6 = 5;

         int modulo = 40%2;

         System.out.println("El resultado de este módulo es: " + modulo + ".");




    }
    
}
